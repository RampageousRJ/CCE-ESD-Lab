	AREA RESET, DATA, READONLY
	EXPORT __Vectors
	
__Vectors 
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	LDR R1,=SRC
	LDR R2,=DST
	MOV R3,#10
	EXPORT Up
	
Up 
	LDR R4,[R1],#4
	STR R4,[R2],#4
	SUB R3,R3,#1
	TEQ R3,#0
	BNE Up
	
STOP
	B STOP
	
SRC DCD 1,2,3,4,5,6,7,8,9
	AREA data, DATA, READWRITE
DST DCD 0
		
	END